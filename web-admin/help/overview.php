<h1>Overview: draft &rarr; live &rarr; finished (or draft &rarr; rejected)</h1>

<img class="pet-screenshot" src="help/images/petition_lifecycle.png">

<p>
  A petition won&rsquo;t appear in the admin interface until it has been confirmed &mdash;
  that is, the petition creator has submitted the petition, received an automatic email containing a 
  unique link, and clicked on that link. This is shown as a <strong>draft</strong> petition.
</p>
<p>
  Draft petitions can either be rejected or approved. 
  If you <a href="?page=help&amp;topic=reject_first">reject a draft petition</a>, it will be listed as <strong>rejected</strong> in the admin interface, 
  but will not  appear as a rejected petition on the live site because the petition creator still has a chance to revise it. 
  When the petition creator revises and resubmits it, it will reappear as draft in the admin interface; 
  if you <a href="?page=help&amp;topic=reject_second">reject it again</a>, it will be marked as rejected and immediately appear as such on the live site.
  Otherwise, if its creator does not resubmit it, the petition will eventually appear as <strong>rejected</strong>
  (the timeout period varies from council to council).
</p>
<p>
  If you <a href="?page=help&amp;topic=approve">approve a draft petition</a>, it becomes <strong>live</strong>, and members of the public can sign it on your website.
</p>
<p>
  When the petition&rsquo;s deadline is reached, its status changes to <strong>finished</strong>, and signatures are no
  longer accepted. You can easily <a href="?page=help&amp;topic=change_deadline">change the deadline</a> before this occurs.
</p>
<p>
  You can <a href="?page=help&amp;topic=write_response">post responses</a> to the petition at any time, whether it is live or finished.
</p>
<p>
  In general, before petitions appear as draft petitions, you won&rsquo;t see them &mdash; effectively, until they have been
  submitted <strong>and confirmed</strong>, you can&rsquo;t act on them anyway. The one common exception to this is if
  a petition creator contacts you because they never received the email containing the confirmation link. In this
  case you can <a href="?page=help&amp;topic=find_unconfirmed_petition">search across all petitions</a>, including unconfirmed ones.
</p>