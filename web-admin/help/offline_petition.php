
<h1>To create an offline petition</h1>

<p>You may wish to use the petitions site as an online repository for details of completed paper petitions that have been handed in to the council. You can log these as &lsquo;offline petitions&rsquo; in the admin interface.</p>

<p>Members of the public cannot create or sign offline petitions &ndash; they are treated as finished petitions.</p>

<p>If the creator of an online petition is also collecting signatures on paper, then you can 
  <a href="?page=help&amp;topic=offline_signatures">add the total number of offline signatures</a> to their online petition.</p>

<p>To create an offline petition click the &lsquo;Create offline petition&rsquo; link:</p>

<img class="pet-screenshot" src="help/images/pet08image00.jpg">

<p>Enter the details of the offline petition and the number of signatures, and then click the &lsquo;Create&rsquo; button:</p>

<img class="pet-screenshot" src="help/images/pet08image01.jpg">

<p>The petition will then be listed in the Finished list of petitions in both the admin interface and the public website.</p>

<p>You can also write a response to an offline petition (which will be listed on the website, and emailed to the petition creator if they supplied an email address), and update the number of signatures.</p>


