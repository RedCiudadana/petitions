
<h1>To add offline signatures</h1>

<p>A petition creator may also be gathering signatures on paper, i.e., &lsquo;offline&rsquo;. It is possible to add the number of these offline signatories to the total of the online petition.</p>

<p>To add offline signatures, enter the total number in the &lsquo;Number of offline signatures&rsquo; box on the relevant petition&rsquo;s admin page, and then click &lsquo;Update&rsquo;:</p>

<img class="pet-screenshot" src="help/images/sig04image00.png">

<p>You can change the number of offline signatures at a later date, if you need to. &nbsp;When doing this, you should always enter the new total: e.g. if there are already 100, and you have 35 more, enter 135.</p>

<p>The total of offline signatures is shown in the admin interface petitions list, after the number of online signatures. In the example below there are 2 online signatures and 100 offline signatures for the &lsquo;binsonhighstreet&rsquo; petition, shown as &lsquo;2/100&rsquo;.</p>

<img class="pet-screenshot" src="help/images/sig04image01.png">

<p>The number of offline signatures is also shown on the public petitions webpage, underneath the list of online signers:</p>
