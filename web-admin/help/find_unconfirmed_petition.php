<p>This will leave a log under &lsquo;Administrator events and notes&rsquo; on the admin page for that petition which states that Admin confirmed the petition.</p>

<p>The petition is now ready to be approved or rejected.</p>

<p>If you confirm a petition accidentally then you can simply reject it, following the 
  <a href="?page=help&amp;topic=reject_first">Rejecting a Petition</a> process.</p>

<p>NOTE: IF A PETITION CREATOR HAS SPELT THEIR EMAIL ADDRESS INCORRECTLY, THEN THEY WILL NOT APPEAR IN THE DATABASE AT ALL, REGARDLESS OF WHAT YOU SEARCH UNDER. Therefore if you cannot find a record for someone, please ask them to resubmit their petition, ensuring that they have no made any typoes or errors in their email address.</p>
