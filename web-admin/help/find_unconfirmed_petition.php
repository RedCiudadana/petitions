<h1>Finding an unconfirmed petition, and confirming it manually</h1>

<p>Once a petition creator has created a petition on the website then they are sent a confirmation email. Until they click the link in the confirmation email confirming that they wish to petition the council, their petition does not appear in the list of Draft petitions in the admin interface.</p>

<p>This is because most unconfirmed petitions are unconfirmed precisely because the petition creator has decided not to continue with the petition. There is therefore no point in showing these petitions in the list of Draft petitions and cluttering up the admin interface.</p>

<p>However, it is still possible to search for unconfirmed petitions, by searching on the petition creator&rsquo;s name or email address, or petition reference (i.e., the short name) for the petition in question. You may need to do this if a petition creator says that they did not receive the confirmation email, or if they are having trouble with the link.</p>

<p>Note that the search results that you are given will show only the email address, name, short name and creation time for the petition. </p>

<p>If you wish to confirm the petition on behalf of the petition creator, then click the &lsquo;Confirm petition, move to &lsquo;draft&rsquo;&rsquo; button. Note that this does not approve a petition or put it live on the site, it merely moves it into &lsquo;Draft&rsquo; status, as if the petition creator had clicked on the link in the confirmation email.</p>

