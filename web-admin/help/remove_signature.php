
<h1>How to remove signatures</h1>

<p>You may need to remove a signature if it is obviously false, or if a signer gets in touch to ask for their name to be removed.</p>

<p>There are two ways to remove a signature.</p>

<h2>On the relevant petition&rsquo;s admin page</h2>

<p>By the name of each signer there is a checkbox next to the word &lsquo;Delete&rsquo;. For any signer that you wish to remove, tick the checkbox (as shown below) and then click &lsquo;Remove all ticked&rsquo;:</p>

<img class="pet-screenshot" src="help/images/sig02image00.png">

<h2>By searching for the signer&rsquo;s name or email address</h2>

<p>This method is the easiest way to remove the same person&rsquo;s signature from multiple petitions.</p>

<p>Use the admin interface&rsquo;s search box to search for the signer&rsquo;s name or email address. The process is then the same: tick the checkbox by the relevant signature, and click &lsquo;Remove all ticked&rsquo;. </p>

