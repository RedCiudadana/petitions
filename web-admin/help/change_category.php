
<h1>To change the category under which a petition was rejected, after a petition has been rejected</h1>

<p>If you realise that the wrong category was selected during the rejection process, then it is possible to change this on the admin page for that petition. You can change the category for any rejected petition &ndash; whether it has been rejected once or twice.</p>

<p>You will find all rejected petitions under the &lsquo;Rejected&rsquo; section of the admin interface. Click the &lsquo;admin&rsquo; link for the petition in question, then on the admin interface page, simply click the &lsquo;Change&rsquo; button by the current rejection category/ies:</p>

<img class="pet-screenshot" src="help/images/pet06image00.jpg">

<p>This will take you through to a page which will allow you to select new categories, and deselect existing ones, as you wish. You must give a reason why these changes are being made:</p>

<img class="pet-screenshot" src="help/images/pet06image01.jpg">

<p>Finally, click the &lsquo;Change criteria&rsquo; button.</p>

<p>If the petition is shown on the public website under rejected petitions then the category/ies for rejection will update with the new details; however, the petition creator will not be emailed to alert them that this has happened.</p>

